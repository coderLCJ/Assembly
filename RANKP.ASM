EXTRN GRADE:WORD
EXTRN COUNT:WORD
PUBLIC RANKP
PUBLIC RANK

DATA SEGMENT PUBLIC
RANK DW 50 DUP(?)
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
RANKP PROC FAR
MOV AX,DATA
MOV DS,AX
MOV DX,COUNT
MOV BX,0

LOOP1:
MOV AX,GRADE[BX]
MOV  RANK[BX],0
MOV CX,COUNT
LEA SI,GRADE
NEXT:
CMP [si],AX
Jle NO_COUNT
INC  RANK[BX]
NO_COUNT:
ADD SI,2
LOOP NEXT
inc RANK[BX]
ADD BX,2
DEC DX
JNE LOOP1

RET
RANKP ENDP

CODE ENDS
  END  
