PUBLIC INPUT
PUBLIC GRADE
PUBLIC COUNT 
DATA SEGMENT PUBLIC
GRADE DW 50 DUP(?)
COUNT  DW ?
MESS1 DB 'GRADE?$'
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
INPUT PROC FAR
MOV AX,DATA
MOV DS,AX
LEA DX,MESS1
MOV AH,09
INT 21H
MOV SI,0
ENTER:
CALL DECIBIN
INC COUNT
CMP DL,','
JZ STORE
CMP DL,13
JZ EXIT
STORE:
MOV GRADE[SI],BX
ADD SI,2
JMP ENTER
EXIT:
MOV GRADE[SI],BX
MOV DL,0AH
MOV AH,02H
INT 21H
RET

DECIBIN PROC NEAR
 MOV BX,0
NEWCHAR: MOV AH,1
 INT 21H
 MOV DL,AL
 SUB AL,30H
 JL EXIT1
 CMP AL,9D
 JG EXIT1
 CBW
 XCHG AX,BX
 MOV CX,10
 MUL CX
 XCHG AX,BX
 ADD BX,AX
 JMP NEWCHAR
 EXIT1:RET
 DECIBIN ENDP

INPUT ENDP
CODE ENDS
 END  
